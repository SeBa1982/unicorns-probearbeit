    public function updateNotice(Request $request, $id)
    {
       if (!Notice::where('order_id', $id)->first()) {
           Notice::create([
               'order_id' => $id,
               'notiz' => $request->notiz,
           ]);

           return redirect(route('orders'));

       } else {

            $notice = Notice::where('order_id', $id);

            $notice->update([
                'notiz' => $request->notiz,
            ]);

            return redirect(route('orders'));
       }

    }

